/**
 * Renders XBM image files to the screen.
 *
 * @author Rynor < http://www.g15tools.com/forum/viewtopic.php?p=333#p333
 *
 * @param canvas Canvas which will be written to
 * @param data The XBM file data that is to be displayed. Stored in a char array.
 * @param width The width of the image
 * @param height The height of the image
 * @param pos_x The X coordinate the top left of the image is to be drawn
 * @param pos_y The Y coordinate the top left of the image is to be drawn
 *
 */
void g15rx_drawXBM(g15canvas* canvas, unsigned char* data, int width, int height, int pos_x, int pos_y)
{
   int y = 0;
   int z = 0;
   unsigned char byte;
   int bytes_per_row = ceil((double) width / 8);

   int bits_left = width;
   int current_bit = 0;

   for(y = 0; y < height; y ++)
   {
      bits_left = width;
      for(z=0;z < bytes_per_row; z++)
      {
         byte = data[(y * bytes_per_row) + z];
         current_bit = 0;
         while(current_bit < 8)
         {
            if(bits_left > 0)
            {
               if((byte >> current_bit) & 1) g15r_setPixel(canvas, (current_bit + (z*8) + pos_x),y + pos_y,G15_COLOR_BLACK);
               //else g15r_setPixel(canvas, (current_bit + (z*8) + pos_x),y + pos_y,G15_COLOR_WHITE);
               bits_left--;
            }
         current_bit++;
         }
      }
   }
}


